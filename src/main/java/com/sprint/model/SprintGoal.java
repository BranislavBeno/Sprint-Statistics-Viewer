package com.sprint.model;

/**
 * The Class SprintGoal.
 */
public class SprintGoal {

	/** The sprint label. */
	private String sprintLabel = "";

	/** The team name. */
	private String teamName = "";

	/** The sprint goals. */
	private String sprintGoals = "";

	/**
	 * Gets the sprint label.
	 *
	 * @return the sprint label
	 */
	public String getSprintLabel() {
		return sprintLabel;
	}

	/**
	 * Sets the sprint label.
	 *
	 * @param sprintLabel the new sprint label
	 */
	public void setSprintLabel(String sprintLabel) {
		this.sprintLabel = sprintLabel;
	}

	/**
	 * Gets the team name.
	 *
	 * @return the team name
	 */
	public String getTeamName() {
		return teamName;
	}

	/**
	 * Sets the team name.
	 *
	 * @param teamName the new team name
	 */
	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}

	/**
	 * Gets the sprint goals.
	 *
	 * @return the sprint goals
	 */
	public String getSprintGoals() {
		return sprintGoals;
	}

	/**
	 * Sets the sprint goals.
	 *
	 * @param sprintGoals the new sprint goals
	 */
	public void setSprintGoals(String sprintGoals) {
		this.sprintGoals = sprintGoals;
	}

}
